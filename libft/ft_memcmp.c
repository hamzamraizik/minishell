#include "libft.h"

int	ft_memcmp(void *s1, void *s2, size_t n)
{
	size_t			i;
	unsigned char	*p;
	unsigned char	*q;

	i = 0;
	p = (unsigned char *)s1;
	q = (unsigned char *)s2;
	while (i < n)
	{
		if (*p != *q)
			return (*p - *q);
		else
		{
			p++;
			q++;
			i++;
		}
	}
	return (0);
}
